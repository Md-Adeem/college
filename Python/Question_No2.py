# Integer variable
integer_var = 42

# Float variable
float_var = 3.14

# String variable
string_var = "Hello, World!"

# Boolean variable
boolean_var = True

# List variable (mutable)
list_var = [1, 2, 3, 4, 5]

# Tuple variable (immutable)
tuple_var = (1, 2, 3, 4, 5)

# Dictionary variable
dict_var = {"name": "Alice", "age": 30}

# Constant (use uppercase by convention)
PI = 3.14159265359

# Print the values and types of variables/constants
print("Integer:", integer_var, type(integer_var))
print("Float:", float_var, type(float_var))
print("String:", string_var, type(string_var))
print("Boolean:", boolean_var, type(boolean_var))
print("List:", list_var, type(list_var))
print("Tuple:", tuple_var, type(tuple_var))
print("Dictionary:", dict_var, type(dict_var))
print("Constant (PI):", PI, type(PI))
